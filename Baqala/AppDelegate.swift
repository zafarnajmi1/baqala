extension AppDelegate {
   
    func refreshDataOnStartup()  {
        
        setScreenBySession()
        
        //Selected User Type
        self.setMyDefaultAccountType()
    }
    
    
    fileprivate func setScreenBySession() {
        //Reset User Defaults
        Sharedata.shareInfo.isFromSettingVC = false
        
        //Navigate to Specific screen
        if Sharedata.shareInfo.email == nil {
            self.moveToSelectlanguage()
        }
            
        else if Sharedata.shareInfo.emailIsVerfied ?? false  == false {
            self.moveToLogin()
        }
        else {
            self.moveToHome()
        }
    }
    
    func setMyDefaultAccountType() {
        
        if(Sharedata.shareInfo.accountType == "guest") {
            myDefaultAccountType = .guest
        }
        else if( Sharedata.shareInfo.accountType == "buyer"){
            myDefaultAccountType = .buyer
        }
        else if( Sharedata.shareInfo.accountType == "seller"){
            myDefaultAccountType = .seller
        }
        else {
            myDefaultAccountType = .notExist
        }
        
        //Call
        setMyDefaultLanguage()
    }
    
    
    func setMyDefaultLanguage()  {
        
        guard let lang = UserDefaults.standard.string(forKey: "myDefaultLanguage") else {
            return myDefaultLanguage = .en
        }
      
        switch lang {
        case "ar":
           myDefaultLanguage = .ar
        default: // .en
           myDefaultLanguage = .en
        }

    }
    

